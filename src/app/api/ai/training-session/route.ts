export async function POST(req: Request) {
  try {
    const { messages, topic } = await req.json();

    const topicConfig = TRAINING_TOPICS[topic];
    if (!topicConfig) {
      return new Response(JSON.stringify({ error: "موضوع غير صالح" }), {
        status: 400,
        headers: { "Content-Type": "application/json" },
      });
    }

    const systemPrompt = `${topicConfig.prompt}\n\n${PRODUCT_KNOWLEDGE}\n\n${SYSTEM_WRAPPER}`;
    const modelMessages = await convertToModelMessages(messages);

    const result = streamText({
      model: google("gemini-3-flash-preview"),
      system: systemPrompt,
      messages: modelMessages,
    });

    return result.toUIMessageStreamResponse();
  } catch (error) {
    console.error("Training session error:", error);
    return new Response(JSON.stringify({ error: "فشل في بدء الجلسة التدريبية" }), {
      status: 500,
      headers: { "Content-Type": "application/json" },
    });
  }
}
